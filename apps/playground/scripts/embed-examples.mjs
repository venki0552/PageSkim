// Embeds the repo's example pages as string modules so the playground's
// "try an example" buttons work in the browser and on Vercel (where the
// examples/ directory is outside the app's file tree at runtime).
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const root = fileURLToPath(new URL("../../..", import.meta.url));
const outDir = fileURLToPath(new URL("../src/generated/", import.meta.url));
mkdirSync(outDir, { recursive: true });

const examples = [
  { id: "article-infobox", label: "Encyclopedia article" },
  { id: "docs", label: "Docs page" },
  { id: "blog", label: "Blog post" },
  { id: "product", label: "Product page" },
];

const entries = examples.map(({ id, label }) => {
  const html = readFileSync(`${root}/examples/${id}/page.html`, "utf8");
  return `  { id: ${JSON.stringify(id)}, label: ${JSON.stringify(label)}, html: ${JSON.stringify(html)} }`;
});

writeFileSync(
  `${outDir}/examples.ts`,
  `// GENERATED by scripts/embed-examples.mjs — do not edit.\n` +
    `export interface Example { id: string; label: string; html: string }\n` +
    `export const EXAMPLES: Example[] = [\n${entries.join(",\n")},\n];\n`,
);
console.log(`embedded ${examples.length} examples`);
